
namespace DizGame.Source.Random_Stuff
{
    /// <summary>
    /// A class that contains the X and Z values for the border/bounds of the map/world
    /// </summary>
    public class Border
    {
        /// <summary>
        /// the high value of X
        /// </summary>
        public float HighX { get; set; }
        /// <summary>
        /// the low value of X
        /// </summary>
        public float LowX { get; set; }
        /// <summary>
        /// the high value of Z
        /// </summary>
        public float HighZ { get; set; }
        /// <summary>
        /// the low value of Z
        /// </summary>
        public float LowZ { get; set; }
    }
}